function create_pubsub_topic() {
  PS_TOPIC_EXISTS=$(gcloud pubsub topics list \
    --filter="name~$1$" \
    --format="value(name)")

  if [[ -z "$PS_TOPIC_EXISTS" ]] || [[ "$PS_TOPIC_EXISTS" == "" ]]
  then
    gcloud pubsub topics create "$1"
  else
    echo "Topic '$1' already exists. Ignoring."
  fi
}


function parse_yaml {
   local prefix=$2
   local s='[[:space:]]*' w='[a-zA-Z0-9_]*' fs=$(echo @|tr @ '\034')
   sed -ne "s|^\($s\):|\1|" \
        -e "s|^\($s\)\($w\)$s:$s[\"']\(.*\)[\"']$s\$|\1$fs\2$fs\3|p" \
        -e "s|^\($s\)\($w\)$s:$s\(.*\)$s\$|\1$fs\2$fs\3|p"  $1 |
   awk -F$fs '{
      indent = length($1)/2;
      vname[indent] = $2;
      for (i in vname) {if (i > indent) {delete vname[i]}}
      if (length($3) > 0) {
         vn=""; for (i=0; i<indent; i++) {vn=(vn)(vname[i])("_")}
         printf("%s%s%s=\"%s\"\n", "'$prefix'",vn, $2, $3);
      }
   }'
}


